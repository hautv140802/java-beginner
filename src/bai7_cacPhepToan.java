public class bai7_cacPhepToan {
    public static void main(String[] args) {
        int a = 11;
        int b = 5;
        System.out.println("a= " + a);
        System.out.println("b= " + b);

        // phép công
        System.out.println("a + b = " + (a+b));
        // gán biến
        int kq= a + b;
        System.out.println("a + b = " + kq);

        // phép trừ
        System.out.println("a - b = " + (a-b));

        // phép nhân
        System.out.println("a * b = " + (a*b));

        // phép chia
        System.out.println("a / b = " + ((double) a/b));

        // phép chia lấy dư
        System.out.println("a % b = " + (a%b));


    }
}
